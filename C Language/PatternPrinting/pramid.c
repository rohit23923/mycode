#include<stdio.h>
int main()
{
    int i,j,k;
    for (i=1;i<=10;i=i+2)
    {
        {
            for (j=1;j<=i;j++)
            printf(" ");
        }
        for (k=1;k<=10-i;k++)
        {
            printf("%d ",k);
        }
        printf("\n");
    }






































    
}